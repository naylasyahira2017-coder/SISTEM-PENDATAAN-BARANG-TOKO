nama_toko = "TOKO FALAH ENTERPRISE"
print(f"\n========== üçÉ SISTEM PENDATAAN BARANG ‚Äî {nama_toko} üçÉ ==========\n")

barang_list = [
    {"nama": "Mie Instan", "harga": 3000, "stok": 20},
    {"nama": "Gula", "harga": 12000, "stok": 10},
    {"nama": "Beras", "harga": 50000, "stok": 5},
    {"nama": "Odol", "harga": 10000, "stok": 15},
]

def tampilkan_barang():
    print(f"\n=========== DATA BARANG ‚Äî {nama_toko} ===========")
    print("| KODE |      NAMA BARANG      |   HARGA   | STOK |")
    print("----------------------------------------------------")
    for i, b in enumerate(barang_list, start=1):
        print(f"| {i:<4}| {b['nama']:<22}| Rp {b['harga']:<7}| {b['stok']:<4}|")
    print("----------------------------------------------------\n")

def tambah_barang():
    print("\n===== TAMBAH BARANG =====")
    nama = input("Nama barang : ").strip()
    harga = int(input("Harga       : "))
    stok = int(input("Stok        : "))

    barang_list.append({"nama": nama, "harga": harga, "stok": stok})
    print("Barang berhasil ditambahkan!\n")

def edit_barang():
    tampilkan_barang()
    kode = int(input("Masukkan kode barang yang ingin diedit: ")) - 1

    if 0 <= kode < len(barang_list):
        print("Tekan ENTER jika tidak ingin mengubah.")
        
        nama = input("Nama baru  : ")
        harga = input("Harga baru : ")
        stok = input("Stok baru  : ")

        if nama.strip(): barang_list[kode]["nama"] = nama
        if harga.strip(): barang_list[kode]["harga"] = int(harga)
        if stok.strip(): barang_list[kode]["stok"] = int(stok)

        print("Data barang berhasil diubah!\n")
    else:
        print("Kode tidak valid!\n")

def hapus_barang():
    tampilkan_barang()
    kode = int(input("Masukkan kode barang yang ingin dihapus: ")) - 1

    if 0 <= kode < len(barang_list):
        barang_list.pop(kode)
        print("Barang berhasil dihapus!\n")
    else:
        print("Kode tidak valid!\n")

def cari_barang():
    keyword = input("Masukkan kata kunci nama barang: ").lower()
    print("\n===== HASIL PENCARIAN =====")

    found = False
    for b in barang_list:
        if keyword in b["nama"].lower():
            print(f"- {b['nama']} | Harga: Rp {b['harga']} | Stok: {b['stok']}")
            found = True

    if not found:
        print("Barang tidak ditemukan.\n")

while True:
    print(f"============= üçÉ MENU UTAMA ‚Äî {nama_toko} üçÉ =============")
    print("1. Tampilkan Barang üåø")
    print("2. Tambah Barang    üåø")
    print("3. Edit Barang      üåø")
    print("4. Hapus Barang     üåø")
    print("5. Cari Barang      üåø")
    print("6. Keluar           üåø")

    pilihan = input("Pilih menu: ")

    if pilihan == "1": 
        tampilkan_barang()
    elif pilihan == "2": 
        tambah_barang()
    elif pilihan == "3": 
        edit_barang()
    elif pilihan == "4": 
        hapus_barang()
    elif pilihan == "5": 
        cari_barang()
    elif pilihan == "6": 
        print("Terima kasih telah menggunakan program!")
        break
    else:
        print("Pilihan tidak valid!\n")
    


            
        


